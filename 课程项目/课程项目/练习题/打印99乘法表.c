#include <stdio.h>
#include <math.h>

//打印99乘法表
int main() {
	int a = 1, b;
	/*  1*1=1
		1*2=2	2*2=4
		1*3=3	2*3=6	3*3=9*/
	for (; a <= 9; a++) {  //确定高度
		for (b = 1; b <= a; b++) {  //确定宽度，不超过a本身
			printf("%d*%d=%-2d  ", b, a, a * b);
			//这里的-2d表示数值不过不够两位时左对齐，右边补一个空格
				//2表示右对齐，左边补一个空格，这里的2和-2表示占用字符宽度
		}
		printf("\n");
	}
	return 0;
}
